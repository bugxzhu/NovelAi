def get_genre_template(genre: str) -> dict | None:
    """Look up genre template by key. Returns None for custom/unknown genres."""
    return GENRE_TEMPLATES.get(genre)


def get_genre_templates_for_api() -> dict:
    """Return templates for frontend consumption (excludes backend-only fields)."""
    result = {}
    for key, tpl in GENRE_TEMPLATES.items():
        result[key] = {
            "label": tpl["label"],
            "description": tpl["description"],
            "world_defaults": tpl["world_defaults"],
            "character_archetypes": tpl["character_archetypes"],
            "plot_templates": tpl["plot_templates"],
        }
    return result
